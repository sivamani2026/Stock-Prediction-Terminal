import pandas as pd
import numpy as np
from sklearn.preprocessing import MinMaxScaler
from typing import Tuple, Dict, Any

def add_technical_indicators(df: pd.DataFrame) -> pd.DataFrame:
    """
    Adds technical indicators to the stock price DataFrame.
    """
    df = df.copy()
    
    # 5-day and 20-day Simple Moving Averages
    df['SMA_5'] = df['Close'].rolling(window=5).mean()
    df['SMA_20'] = df['Close'].rolling(window=20).mean()
    
    # Daily returns
    df['Daily_Return'] = df['Close'].pct_change()
    
    # 5-day rolling volatility
    df['Volatility_5'] = df['Daily_Return'].rolling(window=5).std()
    
    # Lagged features
    df['Lag_1'] = df['Close'].shift(1)
    df['Lag_2'] = df['Close'].shift(2)
    df['Lag_3'] = df['Close'].shift(3)
    
    # Drop rows with NaN values generated by rolling/lag operations
    df = df.dropna()
    return df

def prepare_ml_data(df: pd.DataFrame, lag_days: int = 5, test_ratio: float = 0.2) -> Tuple[np.ndarray, np.ndarray, np.ndarray, np.ndarray, pd.Index, pd.Index]:
    """
    Prepares features and targets for standard ML models (Linear Regression, Random Forest).
    Uses lagged prices as features to predict the next day's close.
    """
    df_feat = df.copy()
    
    # Create lag features for ML models
    feature_cols = []
    for i in range(1, lag_days + 1):
        col_name = f'Lag_Close_{i}'
        df_feat[col_name] = df_feat['Close'].shift(i)
        feature_cols.append(col_name)
        
    df_feat = df_feat.dropna()
    
    X = df_feat[feature_cols].values
    y = df_feat['Close'].values
    dates = df_feat.index
    
    # Chronological train-test split
    split_idx = int(len(X) * (1 - test_ratio))
    
    X_train, X_test = X[:split_idx], X[split_idx:]
    y_train, y_test = y[:split_idx], y[split_idx:]
    dates_train, dates_test = dates[:split_idx], dates[split_idx:]
    
    return X_train, X_test, y_train, y_test, dates_train, dates_test

def prepare_lstm_data(df: pd.DataFrame, time_steps: int = 10, test_ratio: float = 0.2) -> Tuple[np.ndarray, np.ndarray, np.ndarray, np.ndarray, MinMaxScaler, pd.Index]:
    """
    Prepares input sequences and targets for the LSTM model.
    Scales the data using MinMaxScaler and returns scaled X, y, and the scaler object for inverse transformation.
    """
    # Use Close price for LSTM
    data = df[['Close']].values
    
    # Fit and transform Close price
    scaler = MinMaxScaler(feature_range=(0, 1))
    scaled_data = scaler.fit_transform(data)
    
    # Create temporal sequences
    X, y = [], []
    for i in range(time_steps, len(scaled_data)):
        X.append(scaled_data[i - time_steps:i, 0])
        y.append(scaled_data[i, 0])
        
    X, y = np.array(X), np.array(y)
    
    # Reshape X to fit [samples, time_steps, features]
    X = np.reshape(X, (X.shape[0], X.shape[1], 1))
    
    # Get index for corresponding prediction dates (from index time_steps onwards)
    dates = df.index[time_steps:]
    
    # Chronological train-test split
    split_idx = int(len(X) * (1 - test_ratio))
    
    X_train, X_test = X[:split_idx], X[split_idx:]
    y_train, y_test = y[:split_idx], y[split_idx:]
    dates_test = dates[split_idx:]
    
    return X_train, X_test, y_train, y_test, scaler, dates_test
